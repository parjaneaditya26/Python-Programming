#data types in python 
a=10 #integer
b=3.14 #float
c="hello" #string
d=True #boolean
e=2+3j #complex number
print(type(a)) #output: <class 'int'>
print(type(b)) #output: <class 'float'> 
print(type(c)) #output: <class 'str'>
print(type(d)) #output: <class 'bool'>
print(type(e)) #output: <class 'complex'>

#chr() function is used to convert an integer to a character and ord() function is used to convert a character to an integer.
#A=65  ,Z=90
#a=97  ,z=122

f=65
print(chr(f)) #output: A
g='a'
print(ord(g)) #output: 97

